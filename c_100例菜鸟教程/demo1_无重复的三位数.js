// 题目：有1、2、3、4个数字，能组成多少个互不相同且无重复数字的三位数？都是多少？
// 程序分析：可填在百位、十位、个位的数字都是1、2、3、4。组成所有的排列后再去 掉不满足条件的排列。
var count = 0;
// var i = 1;
// var j = 1;
// var k = 1;
var i,j,k=1;
for(i = 1;i<=4;i++){
  for(j = 1;j<=4;j++){
    for(k=1;k<=4;k++){
      if(i!=j&&i!=k&&j!=k){
      count ++;
      console.log(`组合为：${i} ${j} ${k} 为第 ${count}个`);
    }
    }
    console.log(k);
  }
}
